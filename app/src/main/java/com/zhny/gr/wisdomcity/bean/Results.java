public class Results {


    private String id;
    private String recording_date;
    private String url_auto;
    private String screenshot;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRecording_date() {
        return recording_date;
    }

    public void setRecording_date(String recording_date) {
        this.recording_date = recording_date;
    }

    public String getUrl_auto() {
        return url_auto;
    }

    public void setUrl_auto(String url_auto) {
        this.url_auto = url_auto;
    }

    public String getScreenshot() {
        return screenshot;
    }

    public void setScreenshot(String screenshot) {
        this.screenshot = screenshot;
    }
}
